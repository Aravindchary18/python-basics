# A module is just another Python file.
# We use modules to take code from another file.
# First, we import the file using its file name.
# Example: import tools
# After importing, we must use the file name to call its function.
# Example: tools.greet()
# greet() is the function name written inside tools.py.
# tools.greet() means: run greet() from the tools file.
# The dot (.) connects the file name and the function name.
# () means we are calling (running) the function.
# Modules work like this: filename.function()
# No class or object is needed to use a module function.
#note-if you have any spaces in file name just keep underscore instead of leaving space when mentioning in module like hello_aravind. like this
